even = [2, 4, 6, 8]
odd = [1, 3, 5, 7, 9]

# Minimum Number
print(f"Minimum number in even list is: {min(even)}")
print(f"Minimum number in odd list is: {min(odd)}")

print()

# Maximum Number
print(f"Maximum number in even list is: {max(even)}")
print(f"Maximum number in odd list is: {max(odd)}")

print()

# Length of a list
print(f"Length of even list is: {len(even)}")
print(f"Length of odd list is: {len(odd)}")
